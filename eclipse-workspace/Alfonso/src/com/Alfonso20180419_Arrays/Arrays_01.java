public class Arrays_01 {

	public static void main(String[] args) {

		String[] nombres = { "Juan", "Pedro", "Manolo", "Lucía", "Amelie" };

		for (int i = 0; i < nombres.length; i++) {
			System.out.println("Conozco a alguien llamado: " + nombres[i]);
		}

	}

}
