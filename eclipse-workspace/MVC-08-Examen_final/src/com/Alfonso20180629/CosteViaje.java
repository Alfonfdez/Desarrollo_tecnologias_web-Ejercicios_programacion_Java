package com.Alfonso20180629;

public class CosteViaje {
	
	private int costeHotel;
	private int costeAvion;
	private int costeAlquilerCoche;
	private int costeTotal;
	
	
	//SETTERS AND GETTERS
	public int getCosteHotel() {
		return costeHotel;
	}
	public void setCosteHotel(int costeHotel) {
		this.costeHotel = costeHotel;
	}
	public int getCosteAvion() {
		return costeAvion;
	}
	public void setCosteAvion(int costeAvion) {
		this.costeAvion = costeAvion;
	}
	public int getCosteAlquilerCoche() {
		return costeAlquilerCoche;
	}
	public void setCosteAlquilerCoche(int costeAlquilerCoche) {
		this.costeAlquilerCoche = costeAlquilerCoche;
	}
	public int getCosteTotal() {
		return costeTotal;
	}
	public void setCosteTotal(int costeTotal) {
		this.costeTotal = costeTotal;
	}
	
}
