package com.Alfonso20180702;

public class CostePedido {

	private int costeTipo;
	private float costeSize;
	private float costeCodigo;
	private int costeEntrega;
	private float costeTotal;
	
	
	//SETTERS AND GETTERS
	public int getCosteTipo() {
		return costeTipo;
	}
	public void setCosteTipo(int costeTipo) {
		this.costeTipo = costeTipo;
	}
	public float getCosteSize() {
		return costeSize;
	}
	public void setCosteSize(float costeSize) {
		this.costeSize = costeSize;
	}
	public float getCosteCodigo() {
		return costeCodigo;
	}
	public void setCosteCodigo(float costeCodigo) {
		this.costeCodigo = costeCodigo;
	}
	public int getCosteEntrega() {
		return costeEntrega;
	}
	public void setCosteEntrega(int costeEntrega) {
		this.costeEntrega = costeEntrega;
	}
	public float getCosteTotal() {
		return costeTotal;
	}
	public void setCosteTotal(float costeTotal) {
		this.costeTotal = costeTotal;
	}
	

}
